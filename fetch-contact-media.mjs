// Curates fresh Pexels media for the contact page — deduped against BOTH the
// homepage (media.js) and the onboarding page (onboarding-media.js).
// Run: node fetch-contact-media.mjs
const KEY = process.env.PEXELS_KEY || "IPVJsE6aClRZFz8KpSqWAVL82P2OOJZtZ77ufubkLBz2lOLd4YxF0Lls";
import { readFileSync, writeFileSync } from "node:fs";

const headers = { Authorization: KEY };
const base = new URL(".", import.meta.url);

// collect every photo/video id already used elsewhere on the site
let used = "";
for (const f of ["assets/js/media.js", "assets/js/onboarding-media.js"]) {
  try { used += readFileSync(new URL(f, base), "utf8"); } catch {}
}
const usedPhotoIds = new Set([...used.matchAll(/\/photos\/(\d+)\//g)].map((m) => m[1]));
const usedVideoIds = new Set([...used.matchAll(/\/video-files\/(\d+)\//g)].map((m) => m[1]));
console.log("excluding", usedPhotoIds.size, "photos and", usedVideoIds.size, "videos already on the site");

async function pickVideo(query) {
  const r = await fetch(`https://api.pexels.com/videos/search?query=${encodeURIComponent(query)}&per_page=15&orientation=landscape`, { headers });
  const d = await r.json();
  for (const v of d.videos || []) {
    if (usedVideoIds.has(String(v.id))) continue;
    const files = v.video_files.filter((f) => f.width && f.height).sort((a, b) => b.width - a.width);
    const hd = files.find((f) => f.width <= 1920 && f.width >= 1280) || files[0];
    if (!hd) continue;
    usedVideoIds.add(String(v.id));
    return { mp4: hd.link, poster: v.image };
  }
  return null;
}
async function pickPhoto(query, orientation = "landscape") {
  const r = await fetch(`https://api.pexels.com/v1/search?query=${encodeURIComponent(query)}&per_page=20&orientation=${orientation}`, { headers });
  const d = await r.json();
  for (const p of d.photos || []) {
    if (usedPhotoIds.has(String(p.id))) continue;
    usedPhotoIds.add(String(p.id));
    return { img: p.src.large2x || p.src.large, alt: p.alt || query };
  }
  return null;
}

const media = {};
console.log("fetching contact media...");
media.hero = await pickVideo("british people friendly conversation uk cafe talking smiling");
media.aside = await pickPhoto("welcoming uk community centre office british people", "portrait");
media.cta = await pickPhoto("diverse british young people celebration uk together outdoors");
if (!media.hero) media.hero = await pickPhoto("british people talking friendly uk meeting"); // fallback to image

const out = "// AUTO-GENERATED by fetch-contact-media.mjs — distinct from media.js & onboarding-media.js\nwindow.RISE_CONTACT_MEDIA = " +
  JSON.stringify(media, null, 2) + ";\n";
writeFileSync(new URL("assets/js/contact-media.js", base), out);
console.log("\nWrote assets/js/contact-media.js");
