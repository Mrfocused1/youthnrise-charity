// Curates a SEPARATE set of Pexels media for the get-involved onboarding page,
// guaranteed not to repeat any photo/video already used on the homepage (media.js).
// Run: node fetch-onboarding-media.mjs
const KEY = process.env.PEXELS_KEY || "IPVJsE6aClRZFz8KpSqWAVL82P2OOJZtZ77ufubkLBz2lOLd4YxF0Lls";
import { readFileSync, writeFileSync } from "node:fs";

const headers = { Authorization: KEY };

// gather every photo/video id already used on the homepage
const home = readFileSync(new URL("./assets/js/media.js", import.meta.url), "utf8");
const usedPhotoIds = new Set([...home.matchAll(/\/photos\/(\d+)\//g)].map((m) => m[1]));
const usedVideoIds = new Set([...home.matchAll(/\/video-files\/(\d+)\//g)].map((m) => m[1]));
console.log("excluding", usedPhotoIds.size, "homepage photos and", usedVideoIds.size, "homepage videos");

async function pickVideo(query) {
  const r = await fetch(`https://api.pexels.com/videos/search?query=${encodeURIComponent(query)}&per_page=15&orientation=landscape`, { headers });
  const d = await r.json();
  for (const v of d.videos || []) {
    if (usedVideoIds.has(String(v.id))) continue;
    const files = v.video_files.filter((f) => f.width && f.height).sort((a, b) => b.width - a.width);
    const hd = files.find((f) => f.width <= 1920 && f.width >= 1280) || files[0];
    if (!hd) continue;
    usedVideoIds.add(String(v.id)); // also dedupe against other onboarding picks
    return { mp4: hd.link, poster: v.image };
  }
  return null;
}
async function pickPhoto(query) {
  const r = await fetch(`https://api.pexels.com/v1/search?query=${encodeURIComponent(query)}&per_page=20&orientation=landscape`, { headers });
  const d = await r.json();
  for (const p of d.photos || []) {
    if (usedPhotoIds.has(String(p.id))) continue;
    usedPhotoIds.add(String(p.id));
    return { img: p.src.large2x || p.src.large };
  }
  return null;
}
const video = async (q) => (await pickVideo(q)) || null;
const photoOrVideo = async (vq, pq) => (await pickVideo(vq)) || (await pickPhoto(pq));

const media = { default: null, paths: {} };

console.log("fetching onboarding media (distinct queries)...");
media.default = await video("hands together community teamwork hopeful diverse circle");
media.paths.mentor = await photoOrVideo("adult coaching teenager one to one supportive conversation", "older mentor advising young student");
media.paths.volunteer = await photoOrVideo("volunteers planting trees community garden park", "volunteers cleaning park community day");
media.paths.fundraise = await photoOrVideo("charity fun run marathon crowd cheering finish line", "charity bake sale fundraiser people");
media.paths.partner = await photoOrVideo("diverse business team meeting handshake office collaboration", "corporate volunteers team helping community");

// final fallback so nothing is null
if (!media.default) media.default = await pickPhoto("sunrise hopeful silhouette young person future");

const out = "// AUTO-GENERATED by fetch-onboarding-media.mjs — distinct from homepage media.js\nwindow.RISE_OB_MEDIA = " +
  JSON.stringify(media, null, 2) + ";\n";
writeFileSync(new URL("./assets/js/onboarding-media.js", import.meta.url), out);
console.log("\nWrote assets/js/onboarding-media.js");
console.log("default:", media.default?.mp4 ? "video" : media.default?.img ? "image" : "MISSING");
for (const k of ["mentor", "volunteer", "fundraise", "partner"])
  console.log(k + ":", media.paths[k]?.mp4 ? "video" : media.paths[k]?.img ? "image" : "MISSING");
